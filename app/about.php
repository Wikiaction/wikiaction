			<br>
			<h3>CONCEPTION</h3>
			<p>Notre plateforme permet la création de ces projets collectifs et la coordination de plusieurs parties prenantes. Les engagés ne sont pas tenus de passer à l’acte avant le franchissement d’un nombre minimum de personnes nécessaires à l’efficacité du projet.</p>
			<br>
			<h3>CRÉATION</h3>
			<p>Wikiaction permet la création de projets très variés. Par exemple, les parties prenantes peuvent être indépendantes les unes des autres, ou non. Leurs engagements peuvent être ponctuels, récurrents (pendant une période limitée), ou permanents (ce qui correspondrait à une nouvelle habitude à prendre). On permet en outre la possibilité d’agir avant le franchissement du seuil, ou de dépasser le seuil - tout cela à la discrétion du créateur.</p>
			<br>
			<h3>ÉVOLUTION</h3>
			<p>Les projets collectifs correspondent rarement à la manière dont ils avaient été imaginés au départ. L’évolution du contexte, et surtout le partage des connaissances des parties prenantes tend à faire évoluer le projet. Afin de rendre cette évolution possible, nous avons permis au créateur de modifier les paramètres de son projet.</p>
